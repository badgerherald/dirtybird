<h2>Bo Ryan's family reunion is methodical, effective </h2>


<p>Returning from the annual Ryan family reunion Sunday evening, Wisconsin basketball head coach Bo Ryan described the weekend as a "refreshing break" from the monotony of his regular schedule.</p>

<p>"It was nice to have a chance to be more spontaneous," Ryan said. "I don't get to let loose too often."</p>

<p>After planning out a detailed itinerary of the weekend's events for the family to carry out throughout the weekend, Ryan opened the first day with a vigorous hike through the mountains from 10 a.m. to noon, but expressed frustration when his family was not prepared to transition to lunch promptly at noon.</p>

<p>"If we don't stick to the plan, how can they expect to have any fun?" Ryan said of the situation. "Following the gameplan is what makes it fun."</p>

<p>The highlight of the weekend, however, was the 14th annual Ryan family kickball game Saturday afternoon, which Ryan's team won 1-0 in a low-scoring, defensive fashion Ñ with the 65-year-old Ryan making a number of diving catches on defense to preserve the win.</p>

<p>By the end of the reunion Ryan said he was happy with what his family had accomplished.</p>

<p>"We did what we needed to do," Ryan said. "We all got to talk. Everybody is doing well. They're all healthy, but now it's time to move on and prepare for this week."</p>
